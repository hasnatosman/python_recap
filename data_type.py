"""
there are lots of data types.
by using type() function, we will know the data type.

"""

#int

x = 99
print(x)
print(type(x))

#float

pi = 3.1416
print(pi)
print(type(pi))


#string

name = 'Hasnat'
print(name)
print(type(name))


#bool

x = True
print(x)
print(type(x))

"""
these are the simple data types.
"""

